package org.BasicProgram;

public class DefaultValue {

	byte b;
	short s;
	int i;
	long l;
	float f;
	double d;
	char c;
	boolean bl;
	

	public static void main(String[] args) {
		
		DefaultValue obj= new DefaultValue();
		System.out.println("value of b="+obj.b);
		System.out.println("value of s="+obj.s);
		System.out.println("value of i="+obj.i);
		System.out.println("value of l="+obj.l);
		System.out.println("value of f="+obj.f);
		System.out.println("value of d="+obj.d);
		System.out.println("value of c="+obj.c);
		System.out.println("value of bl="+obj.bl);
	}

}

